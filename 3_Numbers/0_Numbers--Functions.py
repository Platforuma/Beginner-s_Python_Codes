#math function
import math

a = 2.3
b = 5.9
c = 7.5

#ceil
print('Ceil for', a, 'is', math.ceil(a))
print('Ceil for', b, 'is', math.ceil(b))
print('Ceil for', c, 'is', math.ceil(c))
print(' ')

#floor
print('Floor for', a, 'is', math.floor(a))
print('Floor for', b, 'is', math.floor(b))
print('Floor for', c, 'is', math.floor(c))
print(' ')

#pow
d = 2
print('Power raise to 2 is', math.pow(d,2))
print('Power raise to 3 is', math.pow(d,3))
print('Power raise to 4 is', math.pow(d,4))
print(' ')

#sqrt
e = 36
f = 144
g = 200
print('Sqrt of', e, 'is', math.sqrt(e))
print('Sqrt of', f, 'is', math.sqrt(f))
print('Sqrt of', g, 'is', math.sqrt(g))



