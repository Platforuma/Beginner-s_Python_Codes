'''
Create lambda functions for add, sub, mult and div
'''
add = lambda num1, num2: num1 + num2
dif = lambda num1, num2: num1 - num2
mul = lambda num1, num2: num1 * num2
div = lambda num1, num2: num1 / num2

print("Sum :", add(9,3))
print("Difference :", dif(9,3))
print("Product :", mul(9,3))
print("Division :", div(9,3))

print("Sum :", add(232,12))
print("Difference :", dif(100,10))
print("Product :", mul(50,63))
print("Division :", div(789654,2542))


